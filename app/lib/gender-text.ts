/** מיפוי תארים מקצועיים מלשון זכר/כללי ללשון נקבה */
const FEMININE_MAP: Record<string, string> = {
  "פסיכולוג קליני": "פסיכולוגית קלינית",
  "פסיכולוג חינוכי": "פסיכולוגית חינוכית",
  "פסיכולוג שיקומי/רפואי": "פסיכולוגית שיקומית/רפואית",
  "פסיכולוג התפתחותי": "פסיכולוגית התפתחותית",
  "פסיכולוג תעסוקתי": "פסיכולוגית תעסוקתית",
  "יועצ/ת חינוכי": "יועצת חינוכית",
  'עו"ס קליני': 'עו"סית קלינית',
  "מטפל/ת בהבעה ויצירה": "מטפלת בהבעה ויצירה",
  "מטפל מיני": "מטפלת מינית",
  "קרימינולוג קליני": "קרימינולוגית קלינית",
  "פיזיותרפיסט/ית": "פיזיותרפיסטית",
  "מטפל נפשי": "מטפלת נפשית",
  "מטפל": "מטפלת",
};

/**
 * מחזיר את הצורה המותאמת למגדר של תואר מקצועי.
 * אם המגדר הוא "נקבה" והתואר קיים במיפוי — מחזיר לשון נקבה.
 * אחרת מחזיר את המקור כפי שהוא.
 */
export function genderTitle(title: string, gender: string | null | undefined): string {
  if (gender === "נקבה") {
    return FEMININE_MAP[title] ?? title;
  }
  return title;
}

/** ממיר מערך תארים מקצועיים בהתאם למגדר */
export function genderTitles(titles: string[], gender: string | null | undefined): string[] {
  return titles.map((t) => genderTitle(t, gender));
}
